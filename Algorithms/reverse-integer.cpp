// BEGIN: https://discuss.leetcode.com/topic/6005/shortest-code-possible-in-c
class Solution {
public:
    int reverse(int x) {
        long y = f1(x);
        return INT_MIN <= y and y <= INT_MAX ? y : 0;
    }
private:
    long f1(long x) {
        long result = 0;
        while (x) {
            result = 10 * result + x % 10;
            x /= 10;
        }
        return result;
    }
};
// END: https://discuss.leetcode.com/topic/6005/shortest-code-possible-in-c

int main(void) {
	Solution solution;
	int x, result, answer;

	x = -2147483648;
	answer = 0;
	result = solution.reverse(x);
	assert(answer == result);

	x = -1563847412;
	answer = 0;
	result = solution.reverse(x);
	assert(answer == result);

	x = 123;
	answer = 321;
	result = solution.reverse(x);
	assert(answer == result);

	x = -123;
	answer = -321;
	result = solution.reverse(x);
	assert(answer == result);

	cout << "\nPassed All\n";
	return 0;
}
